      <h4>Contribution</h4>


      <p>
      DiMarco does not consider himself disabled by deafness and sees his media profile as an opportunity to bring awareness to Deaf culture. 
      DiMarco also became the second male winner and first Deaf contestant on the hit CW series.
      In 2016, DiMarco won season 22 of ABC's dance competition "Dancing with the Stars." With the guidance of professional partner Peta Murgatroyd, 
      he became the first deaf contestant to take home the coveted Mirror Ball Trophy. Following his DWTS win, DiMarco jumped back in front of the camera, 
      expanding his acting credits with a role on Hulu's "Difficult People" starring opposite Billy Eichner and John Early. Previously, 
      he recurred as "Garrett Banducci" on "Switched at Birth" for Freeform, and has a recurring role on Sundance TV's "This Close."
      DiMarco became a producing member of the 2018 Broadway revival of Mark Medoff's groundbreaking play "Children of a Lesser God." The show, which opened 
      April 11th at Studio 54, was directed by Tony Award winner Kenny Leon ("A Raisin in the Sun," "Fences") and stars Joshua Jackson ("The Affair"), 
      Lauren Ridloff ("Wonderstruck"), and Anthony Edwards (ER). This was DiMarco's first producing credit, bringing a unique perspective as a Deaf artist 
      and invaluable insight to telling such a meaningful story.
